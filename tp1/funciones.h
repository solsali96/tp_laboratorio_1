


/** \brief Sumo dos numeros y devuelvo el valor de la suma
 *
 * \param int ingreso el primer numero
 * \param int ingreso el segundo numero
 * \return int devuelvo el resultado de la suma
 *
 */
int sumar(int,int);

/** \brief Ingreso dos valores y los resto
 *
 * \param int Ingreso el primer valor
 * \param int Ingreso el segundo valor
 * \return int Devuelvo el valor de la resta
 *
 */
int restar(int,int);

/** \brief Ingreso dos valores y los multiplico
 *
 * \param int Ingreso el primer valor
 * \param int Ingreso el segundo valor
 * \return int Devuelvo el resultado de la multiplicacion
 *
 */
int multiplicar(int,int);

/** \brief Ingreso dos numeros y los divido
 *
 * \param int Ingreso el primer valor
 * \param int Ingreso el segundo valor
 * \return float Devuelvo el resultado de la division
 *
 */
float dividir(int,int);

/** \brief Ingreso un numero y realizo el factorial de dicho numero
 *
 * \param int Ingreso el numero a factorizar
 * \return int Devuelvo el resultado del factorial
 *
 */
int factorial(int);

/** \brief Menu de opciones de la calculadora
 *
 * \param void
 * \return void
 *
 */
void menu(void);

/** \brief Pide al usuario el numero que desea ingresar en el programa
 *
 * \param char[] Escribo el mensaje que quiero mostrar al pedir el numero
 * \return int Devuelvo el valor del numero
 *
 */
int pedirNumero(char[]);
